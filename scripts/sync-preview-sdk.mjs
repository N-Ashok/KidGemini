// Sync the preview WYSIWYG runtime from the platform into this app (mirrors
// `sync:brand`). The platform builds `public/preview-sdk.js` (real sdk.js +
// publish overlays + a local mock — see Ariantra-Platform/docs/PRD-PREVIEW-WYSIWYG.md);
// we inline it into the sandboxed preview srcDoc so the preview shows the exact
// published overlays with no backend and no network dependency.
//
// Output is a generated TS module exporting the bundle as a string (JSON.stringify
// handles all escaping). Regenerate whenever the platform SDK/overlays change:
//   npm run sync:preview-sdk

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const src = resolve(here, '../../Ariantra-Platform/public/preview-sdk.js');
const outDir = resolve(here, '../src/generated');
const out = resolve(outDir, 'preview-sdk-bundle.ts');

let bundle;
try {
  bundle = readFileSync(src, 'utf8');
} catch (err) {
  console.error(`[sync-preview-sdk] could not read ${src}: ${err.message}`);
  console.error('[sync-preview-sdk] run `npm run build:preview-sdk` in ../Ariantra-Platform first.');
  process.exit(1);
}

mkdirSync(outDir, { recursive: true });
writeFileSync(
  out,
  `// GENERATED by scripts/sync-preview-sdk.mjs from ../Ariantra-Platform/public/preview-sdk.js — DO NOT EDIT.\n` +
    `// The preview WYSIWYG runtime (real sdk.js + publish overlays + local mock), inlined\n` +
    `// into the sandboxed preview srcDoc so it is self-contained (no network). See\n` +
    `// docs (PRD-PREVIEW-WYSIWYG.md) and src/lib/preview-runtime.ts.\n` +
    `export const PREVIEW_SDK_BUNDLE = ${JSON.stringify(bundle)};\n`,
);
console.log(`[sync-preview-sdk] wrote ${out} (${bundle.length} bytes)`);
